/**
 * Keyboard class stores the current state of specific key presses.
 * All properties are static booleans representing whether a key is currently pressed.
 */
class Keyboard {
  /**
   * Indicates if the left arrow key is pressed.
   * @type {boolean}
   */
  static LEFT = false;

  /**
   * Indicates if the right arrow key is pressed.
   * @type {boolean}
   */
  static RIGHT = false;

  /**
   * Indicates if the up arrow key is pressed.
   * @type {boolean}
   */
  static UP = false;

  /**
   * Indicates if the down arrow key is pressed.
   * @type {boolean}
   */
  static DOWN = false;

  /**
   * Indicates if the spacebar key is pressed.
   * @type {boolean}
   */
  static SPACE = false;

  /**
   * Indicates if the 'F' key is pressed.
   * @type {boolean}
   */
  static F = false;
}
